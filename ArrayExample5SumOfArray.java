public class ArrayExample5SumOfArray {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5, 6, 7};
        int sum = 0;
        for(int i : numbers){
            sum += i;
        }
        System.out.println("Sum of the array number is: "+ sum);




    }
}
